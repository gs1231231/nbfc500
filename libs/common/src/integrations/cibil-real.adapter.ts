// ─── Request / Response Types (TransUnion CIBIL API v2) ───────────────────────

export interface CibilAddress {
  line1: string;
  line2?: string;
  city: string;
  state: string;
  pincode: string;
  category: 'CURRENT' | 'PERMANENT' | 'OFFICE';
}

export interface CibilIdentifier {
  type: 'PAN' | 'AADHAAR' | 'VOTER_ID' | 'PASSPORT' | 'DRIVING_LICENSE';
  value: string;
}

export interface CibilPullRequest {
  /** TransUnion assigned member ID */
  memberId: string;
  /** TransUnion assigned password */
  memberPassword: string;
  /** Unique reference generated by lender for dedup */
  inquiryPurpose: 'ACCOUNT_REVIEW' | 'LOAN' | 'CREDIT_CARD' | 'PRE_SCREEN';
  pullType: 'SOFT' | 'HARD';
  applicant: {
    firstName: string;
    middleName?: string;
    lastName: string;
    dateOfBirth: string; // YYYY-MM-DD
    gender: 'M' | 'F' | 'T';
    phone: string;
    email?: string;
    identifiers: CibilIdentifier[];
    addresses: CibilAddress[];
  };
  /** Internal application reference - stored as inquiry reference */
  applicationId: string;
  /** Timestamp in ISO 8601 */
  requestedAt: string;
}

export interface CibilTradeline {
  accountNumber: string;
  subscriberName: string;
  accountType: string;
  ownershipType: string;
  dateOpened: string;
  dateClosed?: string;
  creditLimit?: number;
  highCredit?: number;
  currentBalance: number;
  amountOverdue: number;
  scheduledMonthlyPayment?: number;
  actualPaymentAmount?: number;
  paymentRating: string;
  paymentHistory: string; // 24-month string e.g. "000000000000000000000000"
  suitFiledStatus?: string;
  writtenOffStatus?: string;
}

export interface CibilEnquiry {
  subscriberName: string;
  inquiryDate: string;
  inquiryPurpose: string;
  amount?: number;
}

export interface CibilPullResponse {
  /** TransUnion control number for the report */
  controlNumber: string;
  /** Reference from CibilPullRequest.applicationId */
  applicationId: string;
  /** HTTP-level status from TU API */
  status: 'SUCCESS' | 'NO_HIT' | 'THIN_FILE' | 'ERROR';
  errorCode?: string;
  errorMessage?: string;
  report?: {
    score: number | null; // null for NO_HIT / THIN_FILE
    scoreVersion: string;
    scoreReasons: string[];
    totalAccounts: number;
    activeAccounts: number;
    closedAccounts: number;
    delinquentAccounts: number;
    totalBalance: number;
    totalOverdue: number;
    enquiriesLast30Days: number;
    enquiriesLast12Months: number;
    enquiriesLast24Months: number;
    maxDpdLast12Months: number;
    maxDpdLast24Months: number;
    hasWriteOff: boolean;
    hasSettlement: boolean;
    oldestAccountAgeMonths: number;
    tradelines: CibilTradeline[];
    enquiries: CibilEnquiry[];
    reportGeneratedAt: string;
    validUntil: string; // typically 30 days from generation
  };
  rawXml?: string; // full raw XML response from TU for archival
}

// ─── Adapter Interface ─────────────────────────────────────────────────────────

export interface ICibilAdapter {
  /**
   * Pull CIBIL report for an applicant.
   * Hard pull is recorded as an inquiry; soft pull is not.
   */
  pull(request: CibilPullRequest): Promise<CibilPullResponse>;

  /**
   * Parse the raw XML from TransUnion into a typed CibilPullResponse.
   * Useful when storing raw XML and re-parsing later.
   */
  parseResponse(rawXml: string, applicationId: string): Promise<CibilPullResponse>;
}

