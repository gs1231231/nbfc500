// ─── Mock Adapter ──────────────────────────────────────────────────────────────

export class MockCibilAdapter implements ICibilAdapter {
  async pull(request: CibilPullRequest): Promise<CibilPullResponse> {
    const score = 720 + Math.floor(Math.random() * 100); // 720-819
    return {
      controlNumber: `TU${Date.now()}`,
      applicationId: request.applicationId,
      status: 'SUCCESS',
      report: {
        score,
        scoreVersion: '2.0',
        scoreReasons: ['LENGTH_OF_CREDIT_HISTORY', 'PAYMENT_HISTORY_ON_TIME'],
        totalAccounts: 3,
        activeAccounts: 2,
        closedAccounts: 1,
        delinquentAccounts: 0,
        totalBalance: 450000,
        totalOverdue: 0,
        enquiriesLast30Days: 1,
        enquiriesLast12Months: 2,
        enquiriesLast24Months: 3,
        maxDpdLast12Months: 0,
        maxDpdLast24Months: 0,
        hasWriteOff: false,
        hasSettlement: false,
        oldestAccountAgeMonths: 48,
        tradelines: [],
        enquiries: [],
        reportGeneratedAt: new Date().toISOString(),
        validUntil: new Date(Date.now() + 30 * 24 * 60 * 60 * 1000).toISOString(),
      },
    };
  }

  async parseResponse(_rawXml: string, applicationId: string): Promise<CibilPullResponse> {
    return this.pull({} as CibilPullRequest).then((r) => ({ ...r, applicationId }));
  }
}

// ─── Factory ───────────────────────────────────────────────────────────────────

export function createCibilAdapter(): ICibilAdapter {
  if (process.env.CIBIL_ADAPTER === 'real') {
    return new RealCibilAdapter();
  }
  return new MockCibilAdapter();
}
